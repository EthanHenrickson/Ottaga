<h2>Our Core Principles</h2>

<h3>Privacy-First Approach</h3>
<p>
    Your privacy is our top priority. We use minimal tracking (only one
    authentication cookie), store data with SOC 2 certified providers, and give
    you full control over your information. You have the right to delete all
    conversations and account data at any time. While we do retain conversation
    data for up to 24 months to improve our service, you can request deletion at
    any time.
</p>

<h3>Clear AI Boundaries</h3>
<p>
    We provide clear boundaries about AI capabilities and limitations. When our
    AI determines you may need professional help, it will always try to direct
    you to appropriate resources rather than attempting to handle crisis
    situations on its own. <strong
        >Our AI is not a replacement for professional therapy, counseling, or
        medical care.</strong
    >
</p>

<h3>Honest Communication</h3>
<p>
    We believe in transparency about what our service can and cannot do. We're
    here to provide supportive conversation and emotional support, but we're
    upfront about when professional help is needed.
</p>

<h3>Continuous Improvement</h3>
<p>
    As a solo developer, I'm committed to constantly improving the service based
    on user feedback and the latest best practices in both technology and mental
    health support.
</p>

