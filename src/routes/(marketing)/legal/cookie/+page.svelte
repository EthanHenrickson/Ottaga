<h1>Browser Storage Policy</h1>

<p><strong>Last Updated: May 31st, 2025</strong></p>

<h2>1. Our Minimal Cookie Approach</h2>

<p>
    Ottaga uses a <strong>privacy-first approach</strong> with minimal browser storage.
    We use only one essential cookie for authentication and rely on browser memory
    storage for everything else, avoiding the tracking and analytics cookies used
    by most websites.
</p>

<h2>2. The One Cookie We Use</h2>

<h3>2.1 Authentication Cookie</h3>
<p>We use a single, essential cookie for:</p>
<ul>
    <li>
        <strong>Session Authentication:</strong> Keeping you securely logged in to
        access protected areas of our Service
    </li>
    <li>
        <strong>Security:</strong> Ensuring only authorized users can access their
        personal conversations and settings
    </li>
</ul>

<p><strong>Cookie Details:</strong></p>
<ul>
    <li><strong>Purpose:</strong> Authentication and access control only</li>
    <li>
        <strong>Duration:</strong> Expires when you log out or after a period of
        inactivity
    </li>
    <li><strong>Scope:</strong> Only used on our domain (ottaga.com)</li>
    <li>
        <strong>Content:</strong> Encrypted session identifier (no personal data
        stored in the cookie)
    </li>
</ul>

<h2>3. What We Use Instead of Tracking Cookies</h2>

<h3>3.1 Browser Memory Storage</h3>
<p>
    For non-authentication purposes, we use browser memory storage (RAM-based
    storage) for:
</p>
<ul>
    <li>
        <strong>User Preferences:</strong> Remembering your settings during your
        current session
    </li>
    <li>
        <strong>Analytics:</strong> PostHog uses browser memory to track page events
        and user interactions
    </li>
    <li>
        <strong>Temporary Data:</strong> Session-based information that doesn't require
        persistence
    </li>
</ul>

<h3>3.2 How This Works</h3>
<p>Browser memory storage:</p>
<ul>
    <li>
        Is automatically cleared when you close your browser tab or restart your
        browser
    </li>
    <li>Cannot be accessed by other websites</li>
    <li>
        Does not persist between browser sessions (unlike our authentication
        cookie)
    </li>
    <li>Does not track you across different websites</li>
</ul>

<h2>4. PostHog Analytics Storage</h2>

<p>Our analytics provider, PostHog, may use browser memory storage to:</p>
<ul>
    <li>Track your session while you use our Service</li>
    <li>Remember analytics preferences during your visit</li>
    <li>
        Store temporary identifiers that are cleared when you close your browser
    </li>
</ul>
<p>
    This storage is used solely for understanding how our Service is used and
    improving user experience.
</p>

<h2>5. Benefits of Our Minimal Approach</h2>

<p>
    By using only one essential cookie and memory storage for everything else,
    we provide you with:
</p>
<ul>
    <li>
        <strong>Enhanced Privacy:</strong> No tracking or analytics cookies that
        persist across sessions
    </li>
    <li>
        <strong>Automatic Cleanup:</strong> Non-essential data is automatically cleared
        when you close your browser
    </li>
    <li>
        <strong>Simplified Privacy:</strong> No complex cookie consent banners for
        non-essential cookies
    </li>
    <li>
        <strong>Reduced Tracking:</strong> We cannot track you across different websites
        or build long-term profiles
    </li>
    <li>
        <strong>Essential Functionality:</strong> Authentication cookie ensures secure
        access to your account
    </li>
</ul>

<h2>6. Managing Browser Storage and Cookies</h2>

<h3>6.1 Authentication Cookie Management</h3>
<ul>
    <li>
        <strong>Automatic logout:</strong> Cookie expires after period of inactivity
    </li>
    <li>
        <strong>Manual logout:</strong> Use the logout button to immediately clear
        your authentication cookie
    </li>
    <li>
        <strong>Browser settings:</strong> You can delete cookies through your browser
        settings (this will log you out)
    </li>
</ul>

<h3>6.2 Memory Storage Management</h3>
<p>Since we use browser memory storage for non-essential data:</p>
<ul>
    <li>
        <strong>No action needed:</strong> Storage is automatically cleared when
        you close your browser
    </li>
    <li>
        <strong>Session-based:</strong> Data only exists during your active use of
        our Service
    </li>
    <li>
        <strong>Browser restart:</strong> Completely clears all temporary stored
        data
    </li>
</ul>

<h3>6.3 Browser Settings</h3>
<p>
    You can manage our authentication cookie and storage through your browser
    settings:
</p>
<ul>
    <li>
        <strong>Chrome:</strong> Settings > Privacy and Security > Cookies and other
        site data
    </li>
    <li>
        <strong>Firefox:</strong> Settings > Privacy & Security > Cookies and Site
        Data
    </li>
    <li>
        <strong>Safari:</strong> Safari > Preferences > Privacy > Manage Website
        Data
    </li>
    <li>
        <strong>Edge:</strong> Settings > Cookies and site permissions > Cookies
        and site data
    </li>
</ul>
<p>
    <strong>Note:</strong> Deleting our authentication cookie will log you out of
    your account.
</p>

<h2>6. Third-Party Storage</h2>

<h3>6.1 PostHog Analytics</h3>
<p>PostHog may use browser storage for analytics purposes. This storage:</p>
<ul>
    <li>Is limited to analytics and user experience data</li>
    <li>Does not contain personal conversations or sensitive information</li>
    <li>Is governed by PostHog's privacy practices</li>
    <li>Can be disabled through browser settings if desired</li>
</ul>

<h3>6.2 No Other Third Parties</h3>
<p>
    We do not use any other third-party services that store data in your
    browser.
</p>

<h2>7. Technical Details</h2>

<p>For transparency, here's what we do and don't store:</p>

<h3>What We Store:</h3>
<p><strong>In our authentication cookie:</strong></p>
<ul>
    <li>Encrypted session identifier for secure login</li>
</ul>
<p><strong>In browser memory only:</strong></p>
<ul>
    <li>User interface preferences during your session</li>
    <li>Analytics data for improving our Service</li>
    <li>Temporary session data</li>
</ul>

<h3>What We Never Store:</h3>
<ul>
    <li>Tracking or advertising cookies</li>
    <li>Cross-site tracking data</li>
    <li>Long-term behavioral profiles</li>
    <li>Personal conversation content in browser storage</li>
    <li>Third-party marketing cookies</li>
</ul>

<h2>8. Data Location</h2>

<p>
    All browser storage occurs locally on your device and is not transmitted to
    our servers except:
</p>
<ul>
    <li>When necessary for Service functionality (like authentication)</li>
    <li>For analytics purposes as described in our Privacy Policy</li>
</ul>

